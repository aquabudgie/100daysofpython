def end_game(win):
    end_message = "Congratulations, you win!" if win is True else "GAME OVER"
    play_again = input(f"{end_message}\nWould you like to play again, y or n? ")
    if is_valid_yn(play_again):
        if play_again.lower() == "y":
            start_game()
        else:
            print("Thank you for playing!")
            time.sleep(2)
            exit()
    return


def process_encounter(encounter):
    print(encounters[encounter]["prompt"])
    options = encounters[encounter]["choices"].keys()
    choice = process_input(options)
    result = encounters[encounter]["choices"][choice]["result"]
    print(f"\n{result}")
    outcome = encounters[encounter]["choices"][choice]["correct"]
    if outcome is True:
        pass
    else:
        end_game(outcome)
    return outcome


def process_input(options):
    input_request = ""
    for i, option in enumerate(options):
        if i == len(options) - 1:
            input_request += " or "
        elif i > 0:
            input_request += ", "
        input_request += option
    choice = input(f"Will you choose {input_request}? ")
    if choice.lower() not in options:
        print("Please choose a valid option.")
        return process_input(options)
    return choice


def is_valid_yn(input):
    if input.lower() in ("y", "n"):
        return True
    else:
        return False


